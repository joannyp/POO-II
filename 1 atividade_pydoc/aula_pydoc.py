def sub(v1, v2):
    '''

    :param v1: int
        valor do tipo inteiro
    :param v2: int
        valor do tipo inteiro
    :raise:
        so aceita valores do tipo inteiro

    :return:
        vai retornar a subtracao dos dois valores passados como argumento
    '''

    return (v1 - v2)



